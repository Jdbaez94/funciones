#Números primos gemelos: Son pares de números primos que tienen una diferencia de 2 unidades entre ellos. Por ejemplo, los pares (3,5), (5,7) y (11,13) son primos gemelos, ya que ambos números son primos y su diferencia es exactamente 2.
#Números primos palindrómicos: Son números primos que además se leen igual de izquierda a derecha y de derecha a izquierda. Por ejemplo, 131 y 757 son primos palindrómicos porque cumplen ambas condiciones.

